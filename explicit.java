class explicit {
    
    public static void main(String[] args) {
        byte b = 8; //8 bit
        int a = 8;//32 bit
        double d = 88.8; //64 bit
        char c = 'C'; // 16 bit

        int i = (int)d;            // explicit conversion supports compatible and incompatible data types.
        System.out.println(i);


    }
}
